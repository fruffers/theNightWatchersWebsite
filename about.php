                    <h2>||| FAQ |||</h2>
                    <p>
                        <h5>Why do some characters have longer hair?</h5> The characters with longer hair usually have more money. In Werth
                        (the world TNW takes place in) it is generally the rich who can afford the products to care for longer hair and have more time to bath. Serfs (famers etc.) do a lot of manual labour so they will often cut their hair short
                        for the practicality of it not getting in their way. Hats are also very popular to keep hair from needing to be cleaned as often.
                        Thus long hair is a symbol of high status.
                    </p>

                    <p>
                        <h5>What's the significance of the colours that the characters wear?</h5>Again, this correlates with the amount of money the characters
                        have and the dyes they can afford. Red, purple, and yellow dyes are expensive whereas green and blue dyes are the cheapest. Although
                        some characters may also wear colours that allow one to identify them or symbolise something about them; such as Maude who wears darker sleeves to connote her moral ambiguity
                        and red pouches to connote her anger. Woth's strong association with the colour purple resembles his pride. And finally, the characters who
                        wear red will mostly all desire power in some form.
                    </p>

                    <p><h5>Why do Woth's eyebrows show over his helmet?</h5>Shh! Don't question that. They help show his expressions.</p>

                    <p><h5>What are the different magic types?</h5>Grey (studied), Blue (monster), Black (demons/dead), White (only used by Gods)</p>

                    <p>
                        <h5>What is the whole 'red blood' and 'blue blood' thing about?</h5>Monsters have blue blood because of the magic which is within it. This magic makes them mutated (in the way that they are large and may have abilities)
                        and powerful. But the fact that they have this magic within them makes them unable to eat other Monsters because the different magic will hurt them by reacting violently with their own, which is why they eat True. 'True' (humans and animals) on the other hand have no magic in their blood, therefore it is red. But they can learn
                        grey magic which will be conjured outside their body.
                    </p>

                    <p>
                        <h5>Huh? Insects don't have blood so what are they classed as?</h5>
                        Insects are 'True' because they have no magic. If they were a monster they would be much bigger because of their magic mutations and therefore need to have blood. But if a small insect was a monster then the 
                        same rule would apply; their fluid/hemolymph would be blue. 
                    </p>

                    <p>
                        <h5>What are 'subhumans'?</h5>
                        Subhumans are humanoids which are True but are not pure human (Homo sapien). They are a different species and may be half lizard, or cat etc. Subhumans have just evolved differently. They are regarded to be inferior to pure humans.
                    </p>

