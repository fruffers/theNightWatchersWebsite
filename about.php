                        The Upper-Realm, Middle-Realm, Under-Realm, and Purgatory. The Upper-Realm is a paradise where all who have done
                        no significant wrong in their life go. The Middle-Realm is where the living are (and apparitions). The Lower-Realm is
                        where those who have done significant wrong in their life go and become demons. Purgatory is where the dead are judged for their sins in life, they are either reborn into the middle realm
                        in a new body or sent to the upper or lower realm.
                        However, it is not known if the Upper-Realm and Purgatory really exists,
                        but most True believe that they do. And some mages have even been known to create their own small realms.
