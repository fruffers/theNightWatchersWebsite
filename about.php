                    <h2>About TNW</h2>
                    <p>
                        The Night Watchers is a fantasy webcomic. TNW takes place in Elbion, a fantasy land of monsters, magic, and those who fear both of those things.
                        TNW follows the story of the Woth Wynton, the 'Dragon Slayer'. A large and tremendously strong man who is as arrogant as he tries to be polite.
                        Woth might appear unruly and even frightening, but his mind is soft; for he desperately wants to become just like his idol the honourable fictional knight 'Thornheart' and earn the
                        praise of the kingdom's people. Woth was once the most respected warrior throughout Elbion, earning his name after striking down the fiendish dragon Smog. In these flowering days Woth could kill the most terrifying monsters with ease.
                        But he unexpectedly lost his power and with it the people's respect;
                        now they laugh and insult him as he walks by.
                        He looks to regain his position, but has only dirtied his reputation further with his mercenary jobs for the rich, yet fighting is all he knows and loves. Now he begins to wonder whether the people he is sent to kill really are as bad
                        as his contractors' say. And as does his mind ponder the same when it conjures illusions and nightmares that haunt him.
                    </p>

